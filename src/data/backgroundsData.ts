// Function to retrieve user-uploaded assets from Local Storage
export const getUserUploadedBackgrounds = (): BackgroundItem[] => {
  const stored = localStorage.getItem("userBackgrounds");
  return stored ? JSON.parse(stored) : [];
};

// Function to save user-uploaded assets to Local Storage
export const saveUserUploadedBackgrounds = (assets: BackgroundItem[]) => {
  localStorage.setItem("userBackgrounds", JSON.stringify(assets));
};
